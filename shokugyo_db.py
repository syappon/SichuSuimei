def get_shokugyo_diagnosis(
    nichi_kan: str,
    tsuhensei_nen: str,    # 年柱蔵干通変星  ウェイト: 10%
    tsuhensei_tsuki: str,  # 月柱蔵干通変星  ウェイト: 40%（主軸）
    tsuhensei_nichi: str,  # 日柱蔵干通変星  ウェイト: 35%
    tchu_satsu: str,       # 天中殺
    tsuhensei_toki: str = "",  # 時柱蔵干通変星  ウェイト: 15%（任意）
    weights: dict = None,  # ウェイトを外から上書きしたい場合はここで渡す
) -> dict:
    """
    通変星×天中殺から適職診断を生成する。

    ウェイトの意味：
      月柱(40%) = 職業の方向性・環境の向き不向きの主軸
      日柱(35%) = 本質的な能力・内側の武器
      時柱(15%) = 仕事の成果・結果が出る形（独立セクション）
      年柱(10%) = 才能の下地・社会的な見られ方（補足）

    ウェイトはPILLAR_WEIGHTSで一括管理。
    外から変えたい場合は weights={"月柱":0.5, ...} を渡す。
    """
    w = dict(PILLAR_WEIGHTS)  # デフォルトをコピー
    if weights:
        w.update(weights)     # 外部指定があれば上書き

    # 各柱のデータ取得
    ts_tsuki = TSUHENSEI_DB.get(tsuhensei_tsuki, {})  # 月柱（主）
    ts_nichi = TSUHENSEI_DB.get(tsuhensei_nichi, {})  # 日柱
    ts_toki  = TSUHENSEI_DB.get(tsuhensei_toki,  {})  # 時柱
    ts_nen   = TSUHENSEI_DB.get(tsuhensei_nen,   {})  # 年柱（補足）
    tcs_data = TCHUUSETSU_DB.get(tchu_satsu, {})

    # ---- 向いている環境 ----
    # 月柱(40%)・日柱(35%)・天中殺を重み順にブレンド
    # 月柱→天中殺→日柱の順で交互に並べ、重複を除去
    env_tsuki = ts_tsuki.get("env_keywords", [])
    env_nichi = ts_nichi.get("env_keywords", [])
    env_tcs   = tcs_data.get("env_fit", [])

    env_combined = []
    for i in range(max(len(env_tsuki), len(env_tcs), len(env_nichi))):
        if i < len(env_tcs):    env_combined.append(env_tcs[i])    # 天中殺（環境の大枠）
        if i < len(env_tsuki):  env_combined.append(env_tsuki[i])  # 月柱（方向性）
        if i < len(env_nichi):  env_combined.append(env_nichi[i])  # 日柱（個性）
    env_combined = list(dict.fromkeys(env_combined))[:6]

    # ---- 職業例 ----
    # 月柱(40%)ベース → 日柱(35%)で補強 → 時柱(15%)・年柱(10%)で補足
    jobs_tsuki = ts_tsuki.get("jobs", [])
    jobs_nichi = ts_nichi.get("jobs", [])
    jobs_toki  = ts_toki.get("jobs",  [])
    jobs_nen   = ts_nen.get("jobs",   [])

    # ウェイト順に職業を取得：月柱から多く、年柱は少なく
    jobs_combined = list(dict.fromkeys(
        jobs_tsuki[:4] +   # 月柱から最大4つ（40%）
        jobs_nichi[:3] +   # 日柱から最大3つ（35%）
        jobs_toki[:2]  +   # 時柱から最大2つ（15%）
        jobs_nen[:1]       # 年柱から最大1つ（10%）
    ))[:8]

    # ---- 時柱：成果の出し方（独立セクション）----
    seika_msg = ""
    if tsuhensei_toki:
        seika_msg = JIKOKU_SEIKA_MSG.get(tsuhensei_toki, "")

    # ---- 通変星の組み合わせメッセージ ----
    # 月柱×日柱を最優先、次に月柱×年柱、日柱×年柱
    combo_msg = (
        COMBINATION_MSG.get(frozenset([tsuhensei_tsuki, tsuhensei_nichi]))
        or COMBINATION_MSG.get(frozenset([tsuhensei_tsuki, tsuhensei_nen]))
        or COMBINATION_MSG.get(frozenset([tsuhensei_nichi, tsuhensei_nen]))
        or ""
    )

    # ---- 一言まとめ ----
    tcs_modifier = tcs_data.get("modifier", "")
    core_tsuki   = ts_tsuki.get("core", "")
    core_nichi   = ts_nichi.get("core", "")

    tagline = (
        f"「{tcs_modifier}」ことで力を発揮するタイプ。"
        f"{core_tsuki}の資質を仕事の方向軸に、"
        f"{core_nichi}の深みを内側の武器にして、"
        f"{env_combined[0]}で働くと本領が発揮できます。"
        + (f"\n  {combo_msg}" if combo_msg else "")
    )

    return {
        # 入力値
        "tsuhensei": {
            "月柱": tsuhensei_tsuki,
            "日柱": tsuhensei_nichi,
            "時柱": tsuhensei_toki,
            "年柱": tsuhensei_nen,
        },
        "weights": w,
        "tchu_satsu": tchu_satsu,
        # 出力値
        "env": env_combined,
        "env_avoid": tcs_data.get("env_avoid", []),
        "jobs": jobs_combined,
        "seika_msg": seika_msg,       # 時柱：成果の出し方
        "combo_msg": combo_msg,       # 通変星の組み合わせ
        "tagline": tagline,           # 一言まとめ
        # 詳細表示用
        "tcs_traits": tcs_data.get("traits", []),
        "ts_traits_tsuki": ts_tsuki.get("traits", []),
        "ts_traits_nichi": ts_nichi.get("traits", []),
        "ts_core": {
            "月柱": core_tsuki,
            "日柱": core_nichi,
            "時柱": ts_toki.get("core", ""),
            "年柱": ts_nen.get("core", ""),
        },
    }


def format_diagnosis(result: dict) -> str:
    """診断結果を読みやすいテキストに整形する"""
    ts = result["tsuhensei"]
    w  = result["weights"]
    lines = []

    lines.append(f"■ 月柱通変星（{int(w['月柱']*100)}%）：{ts['月柱']}")
    lines.append(f"■ 日柱通変星（{int(w['日柱']*100)}%）：{ts['日柱']}")
    if ts["時柱"]:
        lines.append(f"■ 時柱通変星（{int(w['時柱']*100)}%）：{ts['時柱']}")
    lines.append(f"■ 年柱通変星（{int(w['年柱']*100)}%）：{ts['年柱']}")
    lines.append(f"■ 天中殺：{result['tchu_satsu']}")
    lines.append("")

    lines.append("【向いている環境】")
    for e in result["env"]:
        lines.append(f"  ・{e}")
    lines.append("【避けた方が良い環境】")
    for e in result["env_avoid"]:
        lines.append(f"  ・{e}")
    lines.append("")

    lines.append("【職業例】")
    lines.append("  " + "、".join(result["jobs"]))
    lines.append("")

    if result["seika_msg"]:
        lines.append(f"【成果の出し方（時柱：{ts['時柱']}）】")
        lines.append(f"  {result['seika_msg']}")
        lines.append("")

    lines.append("【一言まとめ】")
    lines.append(f"  {result['tagline']}")

    return "\n".join(lines)


